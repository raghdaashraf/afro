	// Load initial values to form if field values are identical in all selected records
	function LoadMultiUpdateValues() {
		$this->CurrentFilter = $this->GetKeyFilter();

		// Load recordset
		if ($this->Recordset = $this->LoadRecordset()) {
			$i = 1;
			while (!$this->Recordset->EOF) {
				if ($i == 1) {
					$this->title->setDbValue($this->Recordset->fields('title'));
					$this->desc->setDbValue($this->Recordset->fields('desc'));
				} else {
					if (!ew_CompareValue($this->title->DbValue, $this->Recordset->fields('title')))
						$this->title->CurrentValue = NULL;
					if (!ew_CompareValue($this->desc->DbValue, $this->Recordset->fields('desc')))
						$this->desc->CurrentValue = NULL;
				}
				$i++;
				$this->Recordset->MoveNext();
			}
			$this->Recordset->Close();
		}
	}

